/*

Definicion:
Una base de datos es un mecanismo de persistencia que nos permite almacenar la informacion
en el tiempo.

Sistema de bases de datos:
Nos permite administrar de manera coherente las bases de datos, conosido como
(BDMS) Database Management System (Systema de administracion de bases de datos).

Un (BDMS): puede dividirse en tres subsistemas
Sistema de control de archivos: permite almacenar la bd en un medio fisico.
Sistema de administracio interna: permite distribuir las bd en un determinado orden
Sistema de administracio externa: permite mostrar la informacion por medio de una interfaz.

DBA: Administrador de base de datos

*/